def area(a):
    """
    Функция вычисляет площадь квадрата

    a - Длина стороны квадрата
    Возвращаем Площадь квадрата, вычисленная по формуле a^2
    
    """
    return a * a
    # Вычисляем и возвращаем площадь квадрата по формуле a^2

def perimeter(a):
    """
    Функция вычисляет периметр квадрата

    a - Длина стороны квадрата
    Возвращаем Периметр квадрата, вычисленный по формуле 4 * a
    
    """
    return 4 * a
    # Вычисляем и возвращаем периметр квадрата по формуле 4 * a
